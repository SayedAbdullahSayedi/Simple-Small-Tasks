#include<iostream>
using namespace std;

int main() {
    int Numbers = 2024;
    float Decimal_Numbers = 2.34;
    char Alphabetic_Letters = 'O';
    bool TrueAndFalse = false;

    cout << "This is an integer: " << Numbers << endl;
    cout << "This is a decimal: " << Decimal_Numbers << endl;
    cout << "This is a letter: " << Alphabetic_Letters << endl;
    cout << "This is a boolean: " << TrueAndFalse << endl;
    return 0;
}